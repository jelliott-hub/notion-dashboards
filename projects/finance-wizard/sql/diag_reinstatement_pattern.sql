-- Step 3: Compare item_names on reinstatement invoices vs NON-reinstatement invoices
-- to find items UNIQUE to reinstatements
WITH reinstatement_items AS (
  SELECT DISTINCT il.item_ref_name
  FROM raw_quickbooks.invoice_lines il
  JOIN raw_quickbooks.invoices i ON i.id = il.invoice_id
  WHERE i.doc_number IN (
    'M1APLI0008','MJRITA0003','MSAPSE0005','MU03590001','MALLUN0006',
    'EMLASCF0006','MKINGR0046','MHHCRG0006','SO085845','MCJFSS0003'
    -- subset of known reinstatements
  )
),
all_maint_items AS (
  SELECT
    il.item_ref_name,
    COUNT(*) as total_uses,
    COUNT(DISTINCT CASE
      WHEN i.doc_number IN ('M1APLI0008','MJRITA0003','MSAPSE0005','MU03590001','MALLUN0006',
        'EMLASCF0006','MKINGR0046','MHHCRG0006','SO085845','MCJFSS0003')
      THEN i.doc_number
    END) as reinstatement_uses
  FROM raw_quickbooks.invoice_lines il
  JOIN raw_quickbooks.invoices i ON i.id = il.invoice_id
  WHERE il.item_ref_name ILIKE '%maint%'
  GROUP BY il.item_ref_name
)
SELECT
  item_ref_name,
  total_uses,
  reinstatement_uses,
  ROUND(100.0 * reinstatement_uses / NULLIF(total_uses, 0), 1) as reinstatement_pct
FROM all_maint_items
ORDER BY reinstatement_pct DESC, total_uses DESC;


-- Step 4: Check class_ref on reinstatement vs non-reinstatement invoices
-- Class in QB is often used for revenue categorization
SELECT
  CASE WHEN i.doc_number IN (
    'M1APLI0008','MA1PLC0014','SO085845','MADHIS0010','MAEFRM0002',
    'MAHBIO0009','MALLPC0002','MALLUN0006','MCALDA0006','MCALSF0006',
    'MJRITA0003','MSAPSE0005','MU03590001','MHHCRG0006','MKINGR0046'
  ) THEN 'reinstatement' ELSE 'regular' END as category,
  i.class_ref_name,
  COUNT(*) as cnt
FROM raw_quickbooks.invoices i
WHERE i.doc_number LIKE 'M%'  -- maintenance invoices
GROUP BY 1, 2
ORDER BY 1, 3 DESC;


